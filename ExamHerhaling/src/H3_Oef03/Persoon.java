package H3_Oef03;

public class Persoon {
	private String naam;
	private String voornaam;
	
	public Persoon(String naam, String voornaam) {
		this.naam = naam;
		this.voornaam = voornaam;
	}
	
	public String getNaam() {
		return this.naam;
	}
	
	public String getVoornaam() {
		return this.voornaam;
	}

}
